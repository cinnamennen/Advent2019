import importlib
import unittest

a_solve = getattr(importlib.import_module("10.a"), "solve")
b_solve = getattr(importlib.import_module("10.b"), "solve")


class TestAExamples(unittest.TestCase):
    def test_one(self):
        self.assertEqual(8, a_solve([".#..#", ".....", "#####", "....#", "...##"]))

    def test_two(self):
        self.assertEqual(
            33,
            a_solve(
                [
                    "......#.#.",
                    "#..#.#....",
                    "..#######.",
                    ".#.#.###..",
                    ".#..#.....",
                    "..#....#.#",
                    "#..#....#.",
                    ".##.#..###",
                    "##...#..#.",
                    ".#....####",
                ]
            ),
        )

    def test_three(self):
        self.assertEqual(
            35,
            a_solve(
                [
                    "#.#...#.#.",
                    ".###....#.",
                    ".#....#...",
                    "##.#.#.#.#",
                    "....#.#.#.",
                    ".##..###.#",
                    "..#...##..",
                    "..##....##",
                    "......#...",
                    ".####.###.",
                ]
            ),
        )

    def test_four(self):
        self.assertEqual(
            41,
            a_solve(
                [
                    ".#..#..###",
                    "####.###.#",
                    "....###.#.",
                    "..###.##.#",
                    "##.##.#.#.",
                    "....###..#",
                    "..#.#..#.#",
                    "#..#.#.###",
                    ".##...##.#",
                    ".....#.#..",
                ]
            ),
        )

    def test_five(self):
        self.assertEqual(
            210,
            a_solve(
                [
                    ".#..##.###...#######",
                    "##.############..##.",
                    ".#.######.########.#",
                    ".###.#######.####.#.",
                    "#####.##.#.##.###.##",
                    "..#####..#.#########",
                    "####################",
                    "#.####....###.#.#.##",
                    "##.#################",
                    "#####.##.###..####..",
                    "..######..##.#######",
                    "####.##.####...##..#",
                    ".#####..#.######.###",
                    "##...#.##########...",
                    "#.##########.#######",
                    ".####.#.###.###.#.##",
                    "....##.##.###..#####",
                    ".#.#.###########.###",
                    "#.#.#.#####.####.###",
                    "###.##.####.##.#..##",
                ]
            ),
        )


